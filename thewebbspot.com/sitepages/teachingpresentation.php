                    <h1 class="tpm1">ALIGNMENT</h1>
                    <p>Areil Larsen</p>

                    <ul class="tpm2">
                        <li>Alignment is a subtle design principle. Every good website follows basic rules of alignment, meaning it is skillfully arranged to lead the viewer though the webpage. This is accomplished by arranging content on a grid, like this one taken from the Tech Republic website.</li>
                        <li>These grids are ordered according to viewer content scanning habits. In societies like ours, where we read left to right then down, people tend to scan websites in an “F” pattern looking for relevant information. We stop at the information that catches our eye, or stands out from the rest. Proper alignment enhances a viewer’s ability to find that information.</li>
                        <li>So how do we guide our readers?</li>
                        <li>Heavier or bolded fonts are typically viewed first, they shout, “I’m important!” There should also be a clear hierarchy, such as that in the grid shown, so that your eyes know what things are important and what they should notice, and the lines move you from one portion to the next.</li>
                    </ul>
